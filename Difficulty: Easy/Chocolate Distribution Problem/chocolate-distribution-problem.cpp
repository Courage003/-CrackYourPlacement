class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
    int i=0;
    int j=m-1;
    long long d=INT_MAX;
    sort(begin(a),end(a));
    
    while(j<n){
        d=min(d,a[j]-a[i]);
        j+=1;
        i+=1;
    }
    return d;
    
    }   
};

